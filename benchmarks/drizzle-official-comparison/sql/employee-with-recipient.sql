select
  e.id,
  e.last_name as "lastName",
  e.first_name as "firstName",
  e.title,
  e.title_of_courtesy as "titleOfCourtesy",
  to_char(e.birth_date, 'YYYY-MM-DD"T"00:00:00"Z"') as "birthDate",
  to_char(e.hire_date, 'YYYY-MM-DD"T"00:00:00"Z"') as "hireDate",
  e.address,
  e.city,
  e.postal_code as "postalCode",
  e.country,
  e.home_phone as "homePhone",
  e.extension,
  e.notes,
  e.recipient_id as "recipientId",
  r.id as "recipient_id",
  r.last_name as "recipient_lastName",
  r.first_name as "recipient_firstName",
  r.title as "recipient_title",
  r.title_of_courtesy as "recipient_titleOfCourtesy",
  to_char(r.birth_date, 'YYYY-MM-DD"T"00:00:00"Z"') as "recipient_birthDate",
  to_char(r.hire_date, 'YYYY-MM-DD"T"00:00:00"Z"') as "recipient_hireDate",
  r.address as "recipient_address",
  r.city as "recipient_city",
  r.postal_code as "recipient_postalCode",
  r.country as "recipient_country",
  r.home_phone as "recipient_homePhone",
  r.extension as "recipient_extension",
  r.notes as "recipient_notes",
  r.recipient_id as "recipient_recipientId"
from employees e
left join employees r on r.id = e.recipient_id
where e.id = $1
